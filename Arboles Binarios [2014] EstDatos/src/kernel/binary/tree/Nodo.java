package kernel.binary.tree;

public class Nodo {

	private Nodo left;
	private Nodo right;
	private int num;

	public Nodo(int n) {
	}

	public Nodo getLeft() {
		return left;
	}

	public void setLeft(Nodo left) {
		this.left = left;
	}

	public Nodo getRight() {
		return right;
	}

	public void setRight(Nodo right) {
		this.right = right;
	}

	public int getNum() {
		return num;
	}

}
